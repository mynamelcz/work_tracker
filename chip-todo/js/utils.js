  showModal(content) {
    const overlay = Utils.createElement('div', { class: 'modal-overlay' });
    const modal = Utils.createElement('div', { class: 'modal' });
    modal.appendChild(content);
    overlay.appendChild(modal);
    
    const closeModal = () => {
      document.removeEventListener('keydown', handleEsc);
      overlay.remove();
    };
    
    const handleEsc = (e) => {
      if (e.key === 'Escape') {
        closeModal();
      }
    };
    
    overlay.addEventListener('click', (e) => {
      if (e.target === overlay) {
        closeModal();
      }
    });
    
    document.addEventListener('keydown', handleEsc);
    document.body.appendChild(overlay);
    return overlay;
  },

  confirm(message) {
    return new Promise((resolve) => {
      const overlay = Utils.createElement('div', { class: 'modal-overlay' });
      const modal = Utils.createElement('div', { class: 'modal confirm-modal' });
      modal.innerHTML = `
        <p>${message}</p>
        <div class="confirm-buttons">
          <button class="btn btn-secondary" data-action="cancel">取消</button>
          <button class="btn btn-danger" data-action="confirm">确认</button>
        </div>
      `;
      overlay.appendChild(modal);
      document.body.appendChild(overlay);
      
      const cleanup = () => {
        document.removeEventListener('keydown', handleEsc);
        overlay.remove();
      };
      
      const handleEsc = (e) => {
        if (e.key === 'Escape') {
          cleanup();
          resolve(false);
        }
      };
      
      document.addEventListener('keydown', handleEsc);
      
      modal.querySelector('[data-action="cancel"]').addEventListener('click', () => {
        cleanup();
        resolve(false);
      });
      modal.querySelector('[data-action="confirm"]').addEventListener('click', () => {
        cleanup();
        resolve(true);
      });
    });
  },

  downloadFile(content, filename) {
    const blob = new Blob([content], { type: 'application/json' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = filename;
    a.click();
    URL.revokeObjectURL(url);
  },

  readFile(file) {
    return new Promise((resolve, reject) => {
      const reader = new FileReader();
      reader.onload = (e) => resolve(e.target.result);
      reader.onerror = reject;
      reader.readAsText(file);
    });
  }
